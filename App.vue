	function initLock(){
		const lockPwd = uni.getStorageSync('lockPwd');
		// 如果已经有锁屏密码 则每次展示就要设置需要锁屏
		if(lockPwd){
			uni.setStorage({
				key:'isLock',
				data: true
			});
		}
	}
	function toLogin() {
		const uid = uni.getStorageSync('uid');
		if(uid){
			console.log('已登录')
			return;
		}
		uni.login({
			provider: 'weixin',
			success: function(res) {
				uniCloud.callFunction({
					name: "login-or-register",
					data: {
						code:res.code,
					},
					success: async ({
						result
					}) => {
						console.log(result);
						if(result && result._id){
							uni.setStorage({
								key:'uid',
								data: result._id
							});
							console.log('登录成功：'+result._id)
						}
					}
				})
			}
